				if(type == "hex")
				{
					var result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(data);
					
					var rgb = new Array(3);
					rgb['r'] = parseInt(result[1],16);
					rgb['g'] = parseInt(result[2],16);
					rgb['b'] = parseInt(result[3],16);
					
				}
				else if(type == "rgb")
				{
					data = data.replace("rgb(","");
					data = data.replace(")","");
					var result = data.split(",");
				
					var rgb = [];
					rgb[0] = parseInt(result[0]);
					rgb[1] = parseInt(result[1]);
